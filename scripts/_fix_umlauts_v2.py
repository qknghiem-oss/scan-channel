def fix_text(text: str) -> tuple[str, int]:
    """Wendet alle Replacements an. NUR Wortgrenzen-basiert, KEINE Substring-Composites."""
    count = 0
    # Special Word Replacements + Extra Composites (alle wortbasiert)
    all_repls = SPECIAL_REPLACEMENTS + EXTRA_COMPOSITES
    sorted_repls = sorted(all_repls, key=lambda x: -len(x[0]))
    seen = set()  # Verhindere doppelte Ersetzung
    for fro, to in sorted_repls:
        if fro == to or fro in seen:
            continue
        seen.add(fro)
        pattern = r"(?<![A-Za-zäöüÄÖÜß0-9])" + re.escape(fro) + r"(?![A-Za-zäöüÄÖÜß0-9])"
        matches = re.findall(pattern, text)
        if matches:
            text = re.sub(pattern, to, text)
            count += len(matches)
    return text, count


def main():
    root = Path("C:/Users/will_/.claude/projects/01#scanen mit Claude")
    total_files = 0
    total_changes = 0

    for p in root.rglob("*"):
        if not p.is_file() or not should_process(p):
            continue
        try:
            text = p.read_text(encoding="utf-8")
        except Exception:
            continue
        new_text, count = fix_text(text)
        if count > 0:
            p.write_text(new_text, encoding="utf-8")
            print(f"  {count:>4}x {p.relative_to(root)}")
            total_files += 1
            total_changes += count

    print()
    print(f"OK Total: {total_files} Dateien, {total_changes} Ersetzungen")


