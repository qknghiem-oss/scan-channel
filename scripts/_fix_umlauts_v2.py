    if any(part in {"node_modules", "audio_cache", "thumbnails", "_briefings", "__pycache__", "test_screenshots", ".git"} for part in path.parts):
        return False
    if path.suffix not in {".md", ".py", ".js", ".html", ".css", ".json"}:
        return False
    if path.suffix == ".json":
        return path.name in {"_book.json", "_index.json", "channel.config.json"}
